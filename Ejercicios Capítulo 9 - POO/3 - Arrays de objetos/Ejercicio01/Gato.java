public class Gato {
  
  private String nombre;
  private String sexo;
  private int edad;
  private String color;
  private int peso;
  
  public void setNombre(String n) {
    this.nombre = n;
  }
  
  public String getNombre() {
    return this.nombre;
  }
  
  public void setSexo(String s) {
    this.sexo = s;
  }
  
  public String getSexo() {
    return this.sexo;
  }
  
  public void setEdad(int e) {
    this.edad = e;
  }
  
  public int getEdad() {
    return this.edad;
  }
  
  public void setColor(String c) {
    this.color = c;
  }
  
  public String getColor() {
    return this.color;
  }
  
  public void setPeso(int p) {
    this.peso = p;
  }
  
  public int getPeso() {
    return this.peso;
  }
}
